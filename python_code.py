if __name__ == "__main__":
  main()

# The first two lines define a function named "main", It has nothing within it right now, but it will be the place where put the code we want to run when the script is executed.
# The if __name__ == "__main__": part is a special construct in Python that use if statement to check if the script is being run directly (as the main program) or if it is being imported as a module in another script. If it is being run directly, it will call the main() function.
